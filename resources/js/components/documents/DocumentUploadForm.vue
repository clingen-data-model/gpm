<template>
    <form-container>
        <input-row label="Document" :errors="errors.file">
            <input type="file" ref="fileInput">
        </input-row>

        <input-row label="Type" :errors="errors.document_type_id">
            <select v-model="newDocument.document_type_id">
                <option :value="null">Select...</option>
                <option :value="type.id" v-for="type in documentTypes" :key="type.id">
                    {{type.long_name}}
                </option>
            </select>
        </input-row>

        <input-row :errors="errors.notes" label="Notes">
            <textarea name="notes" v-model="newDocument.notes" cols="30" rows="10"></textarea>
        </input-row>
        
        <button-row>
            <button class="btn white" @click="cancel">Cancel</button>
            <button class="btn blue" @click="save">Save</button>
        </button-row>
    </form-container>
</template>
<script>
import { mapState } from 'vuex';
import { isValidationError } from '@/http';

export default {
    props: {
        saveFunction: {
            type: Function,
            required: true
        }
    },
    data() {
        return {
            newDocument: {
                file: null,
                document_type_id: this.documentTypeId,
                notes: null
            },
            errors: {},
        }
    },
    computed: {
        ...mapState({
            documentTypes: state => state.doctypes.items
        }),
        group () {
            return this.$store.getters['groups/currentItemOrNew']
        },
        application () {
            return this.group.expert_panel
        },
        isReviewed () {
            return Boolean(this.newDocument.date_reviewed)
        },
        documentType () {
            if (this.documentTypes.length == 0) {
                return {};
            }
            // return this.documentTypes. 
            return this.$store.getters['doctypes/getItemById'](this.documentTypeId)
        }
    },
    methods: {
        async save() {
            try {
                let data = new FormData();
                Object.keys(this.newDocument)
                    .forEach(key => {
                        const val = this.newDocument[key]
                        if (val == null) return;
                        data.append(key, val);
                    })
                data.append('file', this.$refs.fileInput.files[0]);
                await this.saveFunction(data);

                this.clearForm();
                this.$emit('saved');
            } catch (error) {
                if (isValidationError(error)) {
                    this.errors = error.response.data.errors
                    return;
                }
            }

        },
        cancel() {
            this.clearForm();
            this.$emit('canceled');
        },
        clearForm() {
            this.initNewDocument();
            this.clearErrors();
        },
        initNewDocument() {
            this.$refs.fileInput.value = null;
            this.newDocument = {
                file: null,
                document_type_id: this.documentTypeId,
                notes: null
            }
        },
        clearErrors () {
            this.errors = {}
        }
    },
    mounted() {
        this.$store.dispatch('doctypes/getItems');
    }
}
</script>